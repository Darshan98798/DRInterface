// Helper to write JS object files (Safe for Render)
function writeJsData(filePath, varName, data, isModule = false) {
    try {
        const dir = path.dirname(filePath);
        if (!fs.existsSync(dir)) {
            console.warn(`[FILESYSTEM] Skipping write: Directory ${dir} does not exist.`);
            return;
        }
        let moduleExport = isModule ? `\n\nif (typeof module !== 'undefined' && module.exports) {\n    module.exports = ${varName};\n}` : '';
        const content = `// Auto-generated by Admin CMS\nconst ${varName} = ${JSON.stringify(data, null, 4)};${moduleExport}\n`;
        fs.writeFileSync(filePath, content, 'utf8');
        console.log(`[FILESYSTEM] Sync completed for ${varName}`);
    } catch (e) {
        console.error(`[FILESYSTEM] Error writing ${varName}:`, e.message);
    }
}

