// STARTUP: Seed Supabase with existing gamesData.js if table is empty
// ──────────────────────────────────────────────────────────────────
async function seedSupabaseIfEmpty() {
    const { data, error } = await supabaseClient.from('projects').select('id').limit(1);
    if (error || (data && data.length > 0)) return; // Already has data

    console.log('--> [SEED] Supabase projects table is empty. Seeding from gamesData.js...');
    let gamesList = readJsData(GAMES_DATA_PATH, 'gamesData') || [];

    const rows = gamesList.map((g, i) => ({
        id: g.Url && g.Url.includes('?id=') ? g.Url.split('?id=')[1]
            : (g.Name.toLowerCase().replace(/[^a-z0-9]/g, '').slice(0, 40) + '_' + i),
        name: g.Name,
        type: g.Type || (g.Url && g.Url.startsWith('http') ? 'company' : 'ui-ux'),
        image: g.Image || null,
        url: g.Url || null,
        tags: g.Tags || [],
        figma_url: g.FigmaLink || null,
        owners: g.Owners ? [g.Owners] : [],
        tools: g.Tools || [],
        sort_order: gamesList.length - i,
        intro_text: '',
        assets: {}
    }));

    const { error: insertErr } = await supabaseClient.from('projects').upsert(rows, { onConflict: 'id' });
    if (insertErr) console.error('--> [SEED] Error seeding:', insertErr.message);
    else console.log(`--> [SEED] Successfully seeded ${rows.length} projects into Supabase!`);
}



// Upload endpoint — saves image to Supabase Storage
app.post('/api/upload', upload.single('image'), async (req, res) => {
    console.log("--> [UPLOAD API] Received image upload request.");
    if (!req.file) {
        console.error("--> [UPLOAD API] Error: No file uploaded");
        return res.status(400).json({ error: 'No file uploaded' });
    }

    try {
        const originalName = req.file.originalname.replace(/[^a-zA-Z0-9.\-]/g, '_');
        const filename = `${Date.now()}_${originalName}`;
        console.log(`--> [UPLOAD API] Uploading to Supabase Storage: ${filename} (${req.file.size} bytes)`);

        // Upload the file buffer directly to Supabase Storage
        const { data, error } = await supabaseClient.storage
            .from('project-images')
            .upload(filename, req.file.buffer, {
                contentType: req.file.mimetype,
                upsert: false
            });

        if (error) throw error;

        // Get the public URL for the uploaded file
        const { data: urlData } = supabaseClient.storage
            .from('project-images')
            .getPublicUrl(filename);

        const publicUrl = urlData.publicUrl;
        console.log("--> [UPLOAD API] Success! Supabase URL: " + publicUrl);
        res.json({ success: true, url: publicUrl });

    } catch (e) {
        console.error("--> [UPLOAD API] UPLOAD ERROR:", e);
        res.status(500).json({ error: 'Failed to upload to Supabase Storage: ' + e.message });
    }
});

